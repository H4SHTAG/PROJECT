    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        
        if(judul.getText().equals("") && pengarang.getText().equals("") && tahun.getSelectedItem().equals("Tahun") && bulan.getSelectedItem().equals("Bulan") && tanggal.getSelectedItem().equals("Tanggal") && jumlah.getText().equals("") && harga.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Lengkapi Data Terlebih Dahulu!");
        }else{
            if (tahun.getSelectedItem().equals("Tahun")) {
                JOptionPane.showMessageDialog(null, "Isi Tahun Terlebih Dahulu");
            }else if(bulan.getSelectedItem().equals("Bulan")){
                JOptionPane.showMessageDialog(null, "Isi Bulan Terlebih Dahulu");
            }else if(tanggal.getSelectedItem().equals("Tanggal")){
                JOptionPane.showMessageDialog(null, "Isi Tanggal Terlebih Dahulu");
            }else{
                if(kategori.getSelectedItem().equals("Pilih Kategori")){
                    JOptionPane.showMessageDialog(null, "Pilih Kategori Terlebih Dahulu");
                }else{
                    try{
                    String gori = null;
                        if (kategori.getSelectedItem().equals("Romance")) {
                            gori = "Romance";
                        }else if(kategori.getSelectedItem().equals("Comedy")){
                            gori = "Comedy";
                        }else if(kategori.getSelectedItem().equals("Adventure")){
                            gori = "Adventure";
                        }else if(kategori.getSelectedItem().equals("Horror")){
                            gori = "Horror";
                        }else if(kategori.getSelectedItem().equals("Action")){
                            gori = "Action";
                        }

                        st = on.createStatement();
                        st.executeUpdate("insert into tb_stok set " + "id_novel='" + id_novel.getText() + "',"  + "judul='" + judul.getText()
                        + "'," + "pengarang='" + pengarang.getText() + "'," + "tahun='" + tahun.getSelectedItem() + "-" + bulan.getSelectedItem() + "-" + tanggal.getSelectedItem() + "'," + "stok='" + jumlah.getText() + "'," + "kategori='" + gori + "'," + "harga='" + harga.getText() + "'");
                        tampilData();
                        JOptionPane.showMessageDialog(null, "Data Berhasil Di Input!");
                        reset();
                    } catch (Exception e){
                        e.printStackTrace();
                    }
                }
            }
            reset();
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

