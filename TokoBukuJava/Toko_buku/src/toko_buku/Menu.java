    private void cariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cariKeyReleased
        // TODO add your handling code here:
        try{
            st = on.createStatement();
            tabModel.getDataVector().removeAllElements();
            tabModel.fireTableDataChanged();
            rs = st.executeQuery("select * from tb_stok where id_novel like '%" + cari.getText() + "%' or judul like'%" +
                    cari.getText() + "%' or pengarang like'%" + cari.getText() + "%' or tahun like'%" + cari.getText() + "%'" +
                    "or stok like'%" + cari.getText() + "%' or kategori like'%" + cari.getText() + "%' or harga like'%" + cari.getText() + "%'");
            while(rs.next()){
                Object[] data = {
                    rs.getString("id_novel"),
                    rs.getString("judul"),
                    rs.getString("pengarang"),
                    rs.getString("tahun"),
                    rs.getString("stok"),
                    rs.getString("kategori"),
                    rs.getString("harga")
                };
                tabModel.addRow(data);
            }   
        } catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_cariKeyReleased

