    public void tampilData(){
        try {
            st = on.createStatement();
            tabModel.getDataVector().removeAllElements();
            tabModel.fireTableDataChanged();
            rs = st.executeQuery("select * from tb_stok");
            while(rs.next()){
                Object[] data = {
                    rs.getString("id_novel"),
                    rs.getString("judul"),
                    rs.getString("pengarang"),
                    rs.getString("tahun"),
                    rs.getString("stok"),
                    rs.getString("kategori"),
                    rs.getString("harga") 
                };
            tabModel.addRow(data);
            }
        } catch (Exception e){
            e.printStackTrace();
        }
    }
